namespace CoursesGn.Domain.Enums;

/// <summary>
/// Représente les 8 directions cardinales et intercardinales d'une boussole.
/// Les valeurs correspondent aux angles en degrés (0° = Nord, sens horaire).
/// </summary>
public enum Direction
{
    North = 0,
    NorthEast = 45,
    East = 90,
    SouthEast = 135,
    South = 180,
    SouthWest = 225,
    West = 270,
    NorthWest = 315
}
